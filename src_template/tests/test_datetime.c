#include <stdio.h>
#include <CUnit/Basic.h>
#include "../datetime.h"

void test_can_get_datetime_moment(void);

int main(void)
{
    CU_pSuite suite = NULL;

    if (CU_initialize_registry() != CUE_SUCCESS)
        return CU_get_error();

    suite = CU_add_suite("suite", NULL, NULL);
    if (suite == NULL) {
        CU_cleanup_registry();
        return CU_get_error();
    }

    if (CU_add_test(suite, "can get datetime at the moment",
                    test_can_get_datetime_moment) == NULL) {
        CU_cleanup_registry();
        return CU_get_error();
    }

    CU_basic_set_mode(CU_BRM_VERBOSE);
    CU_basic_run_tests();

    CU_cleanup_registry();
    return CU_get_error();
}

void test_can_get_datetime_moment(void)
{
    char formed[100];
    char out[100];
    int retval;

    time_t t;
    struct tm tm;

    t = time(NULL);
    tm = *localtime(&t);
    sprintf(formed,
            "%04d%02d%02d%02d%02d%02d",
            tm.tm_year + 1900,
            tm.tm_mon + 1,
            tm.tm_mday,
            tm.tm_hour,
            tm.tm_min,
            tm.tm_sec);

    retval = datetime_get_now(out);

    CU_ASSERT_STRING_EQUAL(out, formed);
    CU_ASSERT_TRUE(retval);
}
